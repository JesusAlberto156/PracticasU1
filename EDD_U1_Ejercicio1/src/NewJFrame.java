    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         String tempIndice = JOptionPane.showInputDialog(this,"¿EN QUE INDICE DESEA ELIMINAR?");
         int indice = Integer.parseInt(tempIndice);
         boolean respuesta = objetoVector.eliminar(indice);
         if(respuesta == true){
            JOptionPane.showMessageDialog(this, "SE ELIMINO CON EXITO: " + objetoVector.mostrarEliminado().getNombre());
            
        }else{
            JOptionPane.showMessageDialog(this, "ERROR; INDICE INCORRECTO, NO SE PUDO ELIMINAR");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int indice = Integer.parseInt( jTextField4.getText());
        Empleado encontrado = objetoVector.buscar(indice);
        if(encontrado == null){
            JOptionPane.showMessageDialog(this, "ERROR; INDICE INCORRECTO, NO SE ENCONTRO EL EMPLEADO");
        }else{
            jTextArea1.setText("NOMBRE: " + encontrado.getNombre() + "\n" + "EDAD: " + 
                    encontrado.getEdad() + "\n" + "SUELDO: " + encontrado.getSueldo()); 
        }
    }//GEN-LAST:event_jButton3ActionPerformed

