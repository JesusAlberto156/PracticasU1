    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Empleado datos = new Empleado();
        
        datos.setNombre(jTextField1.getText());
        int tempedad = Integer.parseInt(jTextField2.getText());
        datos.setEdad(tempedad);
        float tempsueldo = Float.parseFloat(jTextField3.getText());
        datos.setSueldo(tempsueldo);
        
        String tempIndice = JOptionPane.showInputDialog(this,"¿EN QUE INDICE DESEA INSERTAR?");
        tempedad = Integer.parseInt(tempIndice);
        //ahorita tempedad tiene guarda la psision (indice) donde se insertará
        boolean respuesta = objetoVector.insertar(datos, tempedad);
        if(respuesta == true){
            JOptionPane.showMessageDialog(this, "SE INSERTO CON EXITO");
            jTextField1.setText("");jTextField2.setText("");jTextField3.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "ERROR; INDICE INCORRECTO, NO SE PUDO INSERTAR");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

